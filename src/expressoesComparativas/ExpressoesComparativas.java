package expressoesComparativas;

public class ExpressoesComparativas {

    public static void main(String[] args) {
        int pao = 10;
        int acucar = 11;

        System.out.println(pao + "==" + acucar + "? " + (pao == acucar));

        System.out.println(pao + "!=" + acucar + "? " + (pao != acucar));

        System.out.println( pao + ">" + acucar + "? "  + (pao > acucar));

        System.out.println( pao + "<" + acucar + "? "  + (pao < acucar));

        System.out.println( pao + ">=" + acucar + "? "  + (pao >= acucar));

        System.out.println( pao + "<=" + acucar + "? "  + (pao <= acucar));

    }
}
