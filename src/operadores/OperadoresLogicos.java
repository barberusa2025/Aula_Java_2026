package operadores;

public class OperadoresLogicos {

    public static void main(String[] args) {

        boolean x = true;
        boolean y = false;

        System.out.println( x && y); // x and y true?
        System.out.println( x || y); // x or y true?
        System.out.println( !x); // not x true?
        System.out.println( !y); // not y true?

    }
}
